package com.vehicletyreread;

public class Tyre {
	private Integer tyId;
	private String tyCompany;
	private Integer tyCount;

	/**
	 * @return the tyId
	 */
	public Integer getTyId() {
		return tyId;
	}

	/**
	 * @param tyId the tyId to set
	 */
	public void setTyId(Integer tyId) {
		this.tyId = tyId;
	}

	/**
	 * @return the tyCompany
	 */
	public String getTyCompany() {
		return tyCompany;
	}

	/**
	 * @param tyCompany the tyCompany to set
	 */
	public void setTyCompany(String tyCompany) {
		this.tyCompany = tyCompany;
	}

	/**
	 * @return the tyCount
	 */
	public Integer getTyCount() {
		return tyCount;
	}

	/**
	 * @param tyCount the tyCount to set
	 */
	public void setTyCount(Integer tyCount) {
		this.tyCount = tyCount;
	}
}
